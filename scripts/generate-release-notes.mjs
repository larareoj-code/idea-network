#!/usr/bin/env node
import { readFileSync, writeFileSync } from "node:fs";
import { execSync } from "node:child_process";
import { join } from "node:path";
import { exit } from "node:process";

const ROOT = new URL("..", import.meta.url).pathname.replace(/^\/([A-Z]:)/, "$1");
const pkg = JSON.parse(readFileSync(join(ROOT, "package.json"), "utf-8"));
const version = pkg.version;
const date = new Date().toISOString().slice(0, 10);

// Get commits since last tag, or last 30 if no tags
let log;
try {
  const lastTag = execSync("git describe --tags --abbrev=0", { cwd: ROOT }).toString().trim();
  log = execSync(`git log ${lastTag}..HEAD --oneline`, { cwd: ROOT }).toString().trim();
} catch {
  log = execSync("git log --oneline -30", { cwd: ROOT }).toString().trim();
}

const lines = log.split("\n").filter(Boolean).map((l) => {
  const [hash, ...rest] = l.split(" ");
  return { hash, msg: rest.join(" ") };
});

const groups = {
  "Phases": [],
  "Merges": [],
  "Fixes": [],
  "Features": [],
  "Other": [],
};

for (const { hash, msg } of lines) {
  const entry = `- ${msg} (\`${hash}\`)`;
  if (/^(Phase|Merge team)/i.test(msg)) groups["Phases"].push(entry);
  else if (/^Merge/i.test(msg)) groups["Merges"].push(entry);
  else if (/^(Fix|Bug)/i.test(msg)) groups["Fixes"].push(entry);
  else if (/^(Add|Update|Bump|Feat)/i.test(msg)) groups["Features"].push(entry);
  else groups["Other"].push(entry);
}

let md = `# Idea Network v${version} — Release Notes\n\n**Date:** ${date}  \n**Demo:** https://idea-network.netlify.app\n\n`;

for (const [section, entries] of Object.entries(groups)) {
  if (entries.length === 0) continue;
  md += `## ${section}\n\n${entries.join("\n")}\n\n`;
}

md += `---\n_Generated by \`npm run release-notes\`_\n`;

const out = join(ROOT, "RELEASE-NOTES.md");
writeFileSync(out, md);
console.log(`\nRelease notes written to RELEASE-NOTES.md (v${version})\n`);
